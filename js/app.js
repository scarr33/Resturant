const openNav = () => {
  document.getElementById("mySidenav").style.width = "300px";
  document.getElementById("mySidenav").style.left = "0px";
};

const closeNav = () => {
  document.getElementById("mySidenav").style.width = "0";
  document.getElementById("mySidenav").style.left = "-300px";
};
